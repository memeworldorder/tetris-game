    gaming_hub_url=$(railway domain --service gaming-hub 2>/dev/null || echo "https://gaming-hub-production.railway.app")
    
    # Test API Gateway
    if curl -f "$api_gateway_url/health" &> /dev/null; then
        print_status "API Gateway is healthy"
